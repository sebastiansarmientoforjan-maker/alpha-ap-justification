/**
 * Test FRQ Generation with Real Student (Ananya)
 *
 * Simulates Ananya completing a Limits quiz with 88%
 */

require('dotenv').config({ path: '.env.local' });

const WEBHOOK_URL = process.env.NEXT_PUBLIC_BASE_URL || 'http://localhost:3000';
const WEBHOOK_SECRET = process.env.TIMEBACK_WEBHOOK_SECRET;

// Real student from TimeBack
const ANANYA = {
  sourcedId: '571c432f-93f2-40af-9093-f6177e6d2dd7',
  givenName: 'Ananya',
  familyName: 'Kakarlapudi',
  email: 'ananya.kakarlapudi@alpha.school'
};

async function testAnanyaFRQ() {
  console.log('\n🧪 Testing FRQ Generation with Real Student\n');
  console.log('════════════════════════════════════════════════════════════\n');

  const payload = {
    event: 'assessment.completed',
    timestamp: new Date().toISOString(),
    student: {
      sourcedId: ANANYA.sourcedId,
      givenName: ANANYA.givenName,
      familyName: ANANYA.familyName,
      email: ANANYA.email
    },
    assessment: {
      title: 'AP Calculus BC - Limits and Continuity Quiz',
      type: 'quiz',
      course: {
        title: 'AP Calculus BC'
      }
    },
    result: {
      score: 0.88,
      scoreStatus: 'fully graded'
    }
  };

  console.log('📋 Student Info:');
  console.log(`   Name: ${ANANYA.givenName} ${ANANYA.familyName}`);
  console.log(`   ID: ${ANANYA.sourcedId}`);
  console.log(`   Email: ${ANANYA.email}\n`);

  console.log('📝 Simulated Assessment:');
  console.log(`   Title: ${payload.assessment.title}`);
  console.log(`   Course: ${payload.assessment.course.title}`);
  console.log(`   Score: ${(payload.result.score * 100).toFixed(0)}%\n`);

  console.log('📤 Sending webhook...\n');

  try {
    const response = await fetch(`${WEBHOOK_URL}/api/webhooks/timeback`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${WEBHOOK_SECRET}`
      },
      body: JSON.stringify(payload)
    });

    const data = await response.json();

    console.log(`📥 Response (${response.status}):\n`);
    console.log(JSON.stringify(data, null, 2));
    console.log();

    if (response.ok && data.triggered) {
      console.log('✅ SUCCESS: FRQ was generated!\n');
      console.log('📊 Details:');
      console.log(`   FRQ ID: ${data.frqId}`);
      console.log(`   Processing Time: ${data.processingTime}ms\n`);

      if (data.blocked) {
        console.log('🔒 Status: BLOCKED');
        console.log(`   Reason: ${data.blockedReason}\n`);
        console.log('ℹ️  Student needs to complete Week 1 training problems first.\n');
      } else {
        console.log('✅ Status: APPROVED (Prerequisites met)\n');
      }

      console.log('════════════════════════════════════════════════════════════');
      console.log('🔔 CHECK YOUR TELEGRAM NOW!');
      console.log('════════════════════════════════════════════════════════════\n');
      console.log('You should receive:');
      console.log('  1. 📱 Notification message');
      console.log('  2. 📄 PDF attachment (generated by Railway)\n');

      if (data.blocked) {
        console.log('⚠️  Note: PDF shows as BLOCKED because Ananya needs to');
        console.log('   complete training first. This is correct behavior.\n');
      }

    } else if (response.ok && !data.triggered) {
      console.log('⚠️  FRQ was NOT triggered\n');
      console.log(`Reason: ${data.reason}\n`);
    } else {
      console.log('❌ ERROR: Request failed\n');
    }

  } catch (error) {
    console.error('❌ Request failed:', error.message);
  }

  console.log('════════════════════════════════════════════════════════════\n');
}

testAnanyaFRQ().catch(error => {
  console.error('Fatal error:', error);
  process.exit(1);
});
